// Seed the real venues behind event experiences that ESPN ingest can't supply
// (racetracks, golf courses, tennis, cricket, MLS grounds, FCS stadiums). These
// are the venues ranked experiences point at, so /rankings can link "Plan this
// trip" for every experience with a real venue. Rows carry name/city/state plus
// the same photo experiences.json uses; they have NO tenant teams (dbVenues
// LEFT JOINs, so they surface with teams: []). Real places only.
//
//   npm run db:seed:event-venues         # (re)generate the SQL
//   npm run db:seed:event-venues:local   # apply to local D1

