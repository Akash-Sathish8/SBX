const exps = JSON.parse(readFileSync(path.join(ROOT, 'public', 'data', 'experiences.json'), 'utf8')).experiences
const imageFor = (name) => {
  const e = exps.find((e) => e.name === name)
  if (!e?.image) throw new Error(`no image for experience "${name}" — run build-experiences.mjs first`)
  return e.image
}

const esc = (s) => String(s).replace(/'/g, "''")
const rows = VENUES.map(([id, name, city, state, exp]) =>
  `INSERT OR REPLACE INTO venues (id, name, city, state, image) VALUES ('${esc(id)}', '${esc(name)}', '${esc(city)}', '${esc(state)}', '${esc(imageFor(exp))}');`,
)

writeFileSync(OUT, ['-- generated by scripts/seed-event-venues.mjs; do not edit', ...rows, ''].join('\n'))
console.log(`Wrote ${path.relative(ROOT, OUT)}: ${VENUES.length} event venues.`)
